# Verdadeiro e falso
# Os valores booleanos truerepresentam falseexatamente o que você acha que eles fazem: truerepresentam algo que é verdadeiro e falserepresentam algo que é falso.

# Nada
# Em Ruby, nilrepresenta “nada”. Tudo em Ruby tem um valor de retorno. Quando um pedaço de código não tem nada para retornar, ele retornará nil. Isso é bem abstrato, mas fará mais sentido conforme você aprende e usa mais Ruby.